  <div class="stack muted panel">
    <p>Esta app esta pensada para sistemas pequenos: menos acciones, mas claridad y cero culpa.</p>
    <p><strong>Hoy</strong> muestra hasta 3 habitos activos y una accion rapida: No, Parcial (0.5) o Hecho (1). Tambien puedes dejar una nota corta del dia.</p>
    <p><strong>Semana</strong> resume 7 dias con puntos minimos y te da una sola sugerencia por habito. Es intencionada y facil de aplicar.</p>
    <p><strong>Mes</strong> calcula consistencia en 30 dias y una tendencia simple (sube, baja, estable) sin graficas complejas.</p>
    <p>Las sugerencias siguen un orden fijo: hacerlo mas facil, mas obvio o mas satisfactorio. No hay rachas infinitas ni presion.</p>
    <p><strong>Nuevo habito</strong> se configura por bloques:</p>
    <p><strong>basico</strong>: nombre del habito, tipo de frecuencia (diario o semanal) y, si es semanal, el objetivo en veces por semana.</p>
    <p><strong>plan</strong>: version de 2 minutos (la accion minima), plan b (alternativa realista) y nota de friccion (lo que suele bloquearte).</p>
    <p><strong>recordatorio</strong>: si lo activas, eliges una o varias horas locales (formato HH:MM, separadas por coma). El recordatorio solo aparece en hoy despues de la hora.</p>
    <p><strong>estado</strong>: activar o pausar el habito. El sistema limita a 3 habitos activos para mantener el foco.</p>
    <p>La base es <a class="link" href="https://www.todostuslibros.com/libros/habitos-atomicos_978-84-18118-03-6" target="_blank" rel="noopener">Atomic Habits</a>: foco en entorno y repeticion, no en motivacion. La teoria no aparece en la interfaz diaria.</p>
